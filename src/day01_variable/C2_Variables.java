package day01_variable;

public class C2_Variables {
	public static void main(String[] args) {
		
		int sayi;//; yazim dilindeki nokta gibidir.java noktali virgulu gorunce kod yaziliminin bittigini anlar.
		sayi=27;
		System.out.println(sayi);
		
		char ilkHarf='M';
		System.out.println(ilkHarf);
		
		double sayiDouble = 5.34;
		System.out.println(sayiDouble);
		
		System.out.println(sayi + sayiDouble);
		System.out.println(sayi + ilkHarf);//ascii kodlari M harfi 77
		
		
	}

}