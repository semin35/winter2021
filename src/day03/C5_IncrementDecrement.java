package day03;

public class C5_IncrementDecrement {

	public static void main(String[] args) {
		int number=10;
		number-=6;
		System.out.println("number= "+number);
		number/=2;
		System.out.println("number= "+number);
		number*=3;
		System.out.println("number= "+number);
		number--;
		System.out.println("number= "+number);
	}

}
